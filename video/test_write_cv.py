import video

man = video.Manager()

vid = video.ReadCV('test.avi')
man.add(vid)

out = video.WriteCV('test_rewrite.avi')
out.source(0,vid)
man.add(out)

for _ in xrange(int(out.fps()*5)): # Do 5 seconds.
  man.nextFrame()
